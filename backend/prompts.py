def get_aki_style_prompt(level, topic="", category="grammar"):
    """
    Generate a prompt based on 'Aki Sensei's Style' validation rules.
    This prompt is designed for generating a *single* high-quality question.
    """
    
    topic_instruction = ""
    if topic:
        topic_instruction = f"- テーマ・トピック: 「{topic}」に関連する問題にすること。"

    # Customize instruction based on category
    category_label = "文法・語彙"
    if category == "vocab":
        category_label = "語彙（単語の意味や使い方）"
    elif category == "grammar":
        category_label = "文法（助詞、接続詞、活用など）"
    elif category == "practice":
        category_label = "会話・文脈練習（会話の穴埋め・応答）"

    return f"""
    あなたはプロの日本語教師であり、JLPT（日本語能力試験）の試験作成委員です。
    以下の「プロの日本語教師が作成した良問」のスタイル・形式・難易度感を完全に模倣して、
    指定されたレベル【 {level} 】の新しい「{category_label}問題」を1問作成してください。
    {topic_instruction}

    ### 【特別指示：practice（会話練習）の場合】
    もしカテゴリーが「会話・文脈練習」なら、問題文は必ず**「AとBの会話形式」**にしてください。
    例：
    A「週末は 何を しましたか。」
    B「友達と 映画を （　　）。」


    ### 【参考にする良問（プロの指導スタイル）】
    1. 問題：テーブルのうえにおさらを（　）ください。
       選択肢：1.とって, 2.ならべて, 3.わって, 4.あらって
       正解：2
       解説：「食事の準備」の文脈です。「お皿を並べる」というコロケーションを問う良問です。
    2. 問題：毎日 ぎゅうにゅうを（　）。
       選択肢：1.よみます, 2.のみます, 3.すみます, 4.かみます
       正解：2
       解説：基本的な動詞の活用と目的語の組み合わせを問う基本問題です。
    3. 問題：学校へ（　）行きますか。
       選択肢：1.どう, 2.いつも, 3.やって, 4.は
       正解：1 (文全体：いつもはどうやって行きますか)
       解説：並べ替え問題の要素を取り入れた、文脈理解を問う問題です。

    ### 【制約事項】
    1. 語彙レベル：必ず指定された【 {level} 】相当の語彙のみを使用すること。
    2. 正解の明確さ：正解は1つだけにし、他は「ありそうだが間違い」な選択肢にすること。
    3. **文脈の明確化**: 正解が一つに定まるよう、必要な文脈（例：「エアコンをつけるので」など）を必ず問題文に含めること。
    4. **論理的一貫性**: answer（正解の選択肢）と explanation（解説）の内容が完全に一致しているか、出力前に内部で確認すること。
    5. **常識チェック**: 一般的な常識に照らし合わせて、不自然な状況（例：暑いのに理由なく窓を閉める）を作らないこと。
    6. **【重要】コロケーション（語の結びつき）の自然さ**:
       - 副詞と述語の組み合わせが自然か必ずチェックすること。
       - 悪い例：「すぐにこわれやすい」（「すぐに」は瞬間的な動作、「こわれやすい」は性質。組み合わせ不自然）
       - 良い例：「すぐにこわれる」（動作）、「とてもこわれやすい」（性質の強調）
       - **「外国人がよく間違える不自然な日本語」を絶対に生成しないこと。**
       - **【最重要】特定の個人名（「Aki先生」など）は絶対に使わないこと。「先生」「S1さん」などの一般的な呼び名を使ってください。**
    7. **【重要】文法形式の整合性**:
       - **空欄 `( )` に入る言葉は、学習者がそのまま入れて文が成立する形（活用形）にすること。**
       - 悪い例：文「ドアを（　）ください。」 選択肢「しめます」（「しめますください」は文法エラー）
       - 良い例：文「ドアを（　）ください。」 選択肢「しめて」（「しめてください」は正解）
       - 良い例：文「ドアを（　）。」 選択肢「しめます」（文末ならマス形OK）
    8. **解説の易しさ**: 解説は必ず「やさしい日本語」で書くこと。「適切」「目的」などの難しい言葉は使わず、「よいです」「〜するため」のように言い換えること。学習者が辞書なしで理解できるレベルにする。
    9. 出力形式：以下のJSONフォーマットのみを出力すること（マークダウン記法は不要）。

    {{
      "question": "問題文をここに記述",
      "options": ["選択肢1", "選択肢2", "選択肢3", "選択肢4"],
      "answer": "正解の選択肢（文字列で記述）",
      "explanation": "なぜその答えになるのか、学習者が納得できる簡潔な解説（日本語）"
    }}
    """
